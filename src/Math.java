
public class Math {
	

	// Addition method
	public static double addition(double x , double y) {
		return x + y;
	}
	
	// Subtraction
	public static double subtraction(double x , double y)  {
		return x - y;
	}
	
	// Multiplication
	public static double multiplication(double x , double y ) {
		return x * y;
	}
	
	// Divide
	public static double divide( double x , double y) {
		return x / y;
	}
	
	
	 
	
}
